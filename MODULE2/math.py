import math

# Useful Functions in the Math Module

# ceil - ceiling (round upward)
print("math.ceil(2.13) =", math.ceil(2.13))  # Output: 3.0

# cos - cosine (radians)
print("math.cos(math.pi) =", math.cos(math.pi))  # Output: -1.0

# degrees - radians to degrees
print("math.degrees(math.pi) =", math.degrees(math.pi))  # Output: 180.0

# exp - exponent base e
print("math.exp(1) =", math.exp(1))  # Output: 2.718281828459045

# factorial - product of [1 .. n]
print("math.factorial(5) =", math.factorial(5))  # Output: 120

# floor - floor (rounds downward)
print("math.floor(2.93) =", math.floor(2.93))  # Output: 2.0

# gcd - greatest common divisor
print("math.gcd(24, 36) =", math.gcd(24, 36))  # Output: 12

# log - logarithm (default is base e); pass base as optional second parameter
print("math.log(math.e) =", math.log(math.e))  # Output: 1.0
print("math.log(8, 2) =", math.log(8, 2))  # Output: 3.0

# log10 - logarithm base 10
print("math.log10(1000) =", math.log10(1000))  # Output: 3.0

# pow - power (exponentiation)
print("math.pow(3, 4) =", math.pow(3, 4))  # Output: 81.0

# radians - degrees to radians
print("math.radians(270.0) =", math.radians(270.0))  # Output: 4.71238898038469

# sin - sine (radians)
print("math.sin(3 * math.pi / 2) =", math.sin(3 * math.pi / 2))  # Output: -1.0

# sqrt - square root
print("math.sqrt(2) =", math.sqrt(2))  # Output: 1.4142135623730951
